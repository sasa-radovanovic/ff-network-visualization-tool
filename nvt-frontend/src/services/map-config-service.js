export function generatePin(color) {
    return new google.maps.MarkerImage("http://chart.apis.google.com/chart?chst=d_map_pin_letter&chld=%E2%80%A2|" + color,
        new google.maps.Size(21, 34),
        new google.maps.Point(0,0),
        new google.maps.Point(10, 34));
}


export function generateInfoWindow(iata, icao, name, city, country, lat, long, color) {

    var contentString = '<div id="content" style="color:'+ color +';" >'+
        '<div id="siteNotice">'+
        '</div>'+
        '<p id="firstHeading" class="firstHeading">' + name + ' (' + iata + '/' + icao + ')</p>'+
        '<div id="bodyContent">'+
        '<p><b>'+ city + ', ' + country + '</b></p>'+
        '<p>Longitude: ' + long + ' Latitude: ' + lat +
        '</p>'+
        '</div>'+
        '</div>';

    return new google.maps.InfoWindow({
        content: contentString
    });
}



